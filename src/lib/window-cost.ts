/**
 * 샷시(창호) 공사비 — 평형대·베이·등급 룩업 테이블.
 *
 * 자재마스터의 window 단가가 아닌 이 표가 진실의 원천.
 * (Obsidian: `BIZ/apt planner cost caliltor logic.md` 의 '평형대별 샷시 공사비' 표)
 *
 * 포함 범위: 거실 이중창 + 안방 발코니 단창·내창 + 작은방 이중창 + 주방창 +
 *             안방 발코니 터닝도어 + 철거비 + 사다리차 양중 + 기업이윤 15%
 *
 * 표(부가세 별도, 원):
 *   ┌────────────────────┬───────────┬───────────┬───────────┐
 *   │ 구분               │ 가성비    │ 표준      │ 고급      │
 *   ├────────────────────┼───────────┼───────────┼───────────┤
 *   │ 20평대 2베이       │ 8,000,000 │ 8,500,000 │11,000,000 │
 *   │ 20평대 3베이       │ 8,800,000 │ 9,500,000 │12,000,000 │
 *   │ 30평대 2베이       │ 9,000,000 │10,200,000 │14,000,000 │
 *   │ 30평대 3·4베이     │ 9,700,000 │11,000,000 │14,300,000 │
 *   │ 40평대 3·4베이     │12,000,000 │14,000,000 │17,000,000 │
 *   └────────────────────┴───────────┴───────────┴───────────┘
 *
 * 등급 자재 스펙(거실 이중창 기준):
 *  - 가성비: KCC 프라임 이중창 VBF 242 24T 로이 + 방충망
 *  - 표준:   LX 뷰 250 24T 로이 + 방충망
 *  - 고급:   LX 뷰 285 베젤리스 26T 슈퍼로이 단열간봉
 */
import type { GradeGroup } from './types';

type SashGroup = '가성비' | '표준' | '고급';

/** 평형대 키 — pyeong 입력값 → '20평대' / '30평대' / '40평대' */
type PyeongBand = '20평대' | '30평대' | '40평대';

/** 베이 구분 키 — 표의 행 구분 (평형대마다 다름) */
type BayKey = '2베이' | '3베이' | '3·4베이';

/** [평형대][베이키][등급] → 총 공사비(원, 부가세 별도) */
const TABLE: Record<PyeongBand, Partial<Record<BayKey, Record<SashGroup, number>>>> = {
  '20평대': {
    '2베이': { '가성비': 8_000_000, '표준': 8_500_000, '고급': 11_000_000 },
    '3베이': { '가성비': 8_800_000, '표준': 9_500_000, '고급': 12_000_000 },
  },
  '30평대': {
    '2베이':   { '가성비': 9_000_000, '표준': 10_200_000, '고급': 14_000_000 },
    '3·4베이': { '가성비': 9_700_000, '표준': 11_000_000, '고급': 14_300_000 },
  },
  '40평대': {
    // 40평대는 2베이가 표에 없음 — 3·4베이 단가로 폴백 (대형 평형은 베이수 무관 큰 시공 규모)
    '3·4베이': { '가성비': 12_000_000, '표준': 14_000_000, '고급': 17_000_000 },
  },
};

/** pyeong → 평형대 키 (15평 이하는 20평대, 40+는 40평대) */
function pyeongBand(pyeong: number): PyeongBand {
  if (pyeong < 30) return '20평대';
  if (pyeong < 40) return '30평대';
  return '40평대';
}

/** GradeGroup → 표의 등급 키 ('단일등급'은 '표준'으로 폴백) */
function sashGroupOf(group: GradeGroup): SashGroup {
  if (group === '가성비' || group === '표준' || group === '고급') return group;
  return '표준';
}

/**
 * 평형대 + 베이수 + 등급 → 샷시 공사비 (부가세 별도, 원).
 * 표에 없는 베이 조합은 같은 평형대 안에서 가장 큰 베이키로 폴백.
 *  - 20평대 4+베이 → 20평대 3베이
 *  - 30평대 5+베이 → 30평대 3·4베이
 *  - 40평대 2베이/5+베이 → 40평대 3·4베이
 */
export function lookupWindowCost(
  pyeong: number,
  bay: number,
  group: GradeGroup,
): number {
  const band = pyeongBand(pyeong);
  const sg = sashGroupOf(group);
  const row = TABLE[band];

  // 평형대별 베이 매핑
  if (band === '20평대') {
    const key: BayKey = bay <= 2 ? '2베이' : '3베이';
    return row[key]?.[sg] ?? 0;
  }
  if (band === '30평대') {
    const key: BayKey = bay <= 2 ? '2베이' : '3·4베이';
    return row[key]?.[sg] ?? 0;
  }
  // 40평대 — 2베이도 3·4베이 단가로 폴백
  return row['3·4베이']?.[sg] ?? 0;
}

/** 등급 라벨 디버깅용 — 사용자에게 노출 안 함 */
export function describeWindowBand(pyeong: number, bay: number): string {
  const band = pyeongBand(pyeong);
  if (band === '20평대') return `20평대 ${bay <= 2 ? '2베이' : '3베이+'}`;
  if (band === '30평대') return `30평대 ${bay <= 2 ? '2베이' : '3·4베이'}`;
  return `40평대 3·4베이`;
}
